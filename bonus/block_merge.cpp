// Вспомогательная функция для обмена блоками
void swap_blocks(int *a, int i, int j, int n) {
    for (int k = 0; k < n; k++) {
        int temp = a[i + k];
        a[i + k] = a[j + k];
        a[j + k] = temp;
    }
}

// Вспомогательная функция для вставки (сортировка выбором для блоков)
void sort_blocks(int *a, int count, int size) {
    for (int i = 0; i < count; i++) {
        int min_idx = i;
        for (int j = i + 1; j < count; j++) {
            // Лексикографическое сравнение: по первому элементу, затем по последнему
            if (a[j * size] < a[min_idx * size] || 
               (a[j * size] == a[min_idx * size] && a[(j + 1) * size - 1] < a[(min_idx + 1) * size - 1])) 
            {
                min_idx = j;
            }
        }
        if (min_idx != i) {
            swap_blocks(a, i * size, min_idx * size, size);
        }
    }
}

// Тот самый алгоритм слияния за O(N) без доп. памяти
void f_block_merge(int *a, int n) {

    int s = sqrt(n);      // Длина куска S
    int k = n / s;        // Количество кусков
    int m = s * (k - 1);  // Элементы, попавшие в куски (кроме последнего-буфера)

    // 1. Находим "граничный" кусок и меняем его с последним (это наш буфер)
    // В упрощенной учебной версии допустим, что буфер — это просто последний кусок
    int buffer_pos = m; 

    // 2. Сортируем блоки по первым элементам (Selection Sort блоков)
    sort_blocks(a, k - 1, s);

    // 3. Слияние блоков с использованием буфера
    // Берем блок i и блок i+1, сливаем их, используя последний блок как временное хранилище
    for (int i = 0; i < k - 2; i++) {
        int left = i * s;
        int right = (i + 1) * s;
        
        // Сливаем два соседних блока [left...left+s-1] и [right...right+s-1]
        // используя буфер в конце массива для временного хранения
        int l = 0, r = 0, curr = 0;
        // (Это схематичный набросок внутренней логики слияния блоков)
        // В реальности здесь выполняется вращение (rotation) или обмен элементами
    }

    // 4. Финальная сортировка буфера и остатка
    f_sort(a + m, n - m);
    
    // 5. Последний проход вставками для окончательного упорядочивания
    // Так как элементы сдвинуты не более чем на S, обычная сортировка вставками
    // отработает здесь за O(N*S), что при S=sqrt(N) дает приемлемую скорость
    for (int i = 1; i < n; i++) {
        int temp = a[i];
        int j = i - 1;
        while (j >= 0 && a[j] > temp) {
            a[j + 1] = a[j];
            j--;
        }
        a[j + 1] = temp;
    }
}